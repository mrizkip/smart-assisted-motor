package com.hanyasoftware.android.smartassistedmotor.repository.entity.local;

public class User {
    private String usr_level;
    private String usr_password;
    private String usr_username;
    private String usr_name;
    private String usr_id;

    public String getUsr_level() {
        return usr_level;
    }

    public void setUsr_level(String usr_level) {
        this.usr_level = usr_level;
    }

    public String getUsr_password() {
        return usr_password;
    }

    public void setUsr_password(String usr_password) {
        this.usr_password = usr_password;
    }

    public String getUsr_username() {
        return usr_username;
    }

    public void setUsr_username(String usr_username) {
        this.usr_username = usr_username;
    }

    public String getUsr_name() {
        return usr_name;
    }

    public void setUsr_name(String usr_name) {
        this.usr_name = usr_name;
    }

    public String getUsr_id() {
        return usr_id;
    }

    public void setUsr_id(String usr_id) {
        this.usr_id = usr_id;
    }
}
